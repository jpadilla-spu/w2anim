        $rgb = sprintf("%02X%02X%02X", $r, $g, $b);
        $rgb_name{$rgb}      = $name;
        $rgb_name{"#$rgb"}   = $name;
        $rgb_code{lc($name)} = "#$rgb";
        $rgb = lc($rgb);
        $rgb_name{$rgb}      = $name;
        $rgb_name{"#$rgb"}   = $name;
    }

    sub get_rgb_name {
        my ($code) = @_;
        if ($rgb_name{$code}) { return $rgb_name{$code}; }
        return uc($code);
    }
    
    sub get_rgb_code {
        my ($name) = @_;
        my $hexform = "^#?[0-9a-f][0-9a-f][0-9a-f][0-9a-f][0-9a-f][0-9a-f]\$";
        if ($name =~ /$hexform/i) { return uc($name); }
        if ($rgb_code{lc($name)}) { return $rgb_code{lc($name)}; }
        return 0;
    }

    sub hex2decimal {
        my ($h) = @_;
        my ($s, $c, $d, $i);
        $s = "0123456789ABCDEF";
        $h = uc($h);
        $c = length($h);
        $d = index($s,substr($h,$c-1,1));
        for ($i=$c-1; $i>0; $i--) {
            $d += 16**$i * index($s,substr($h,$c-$i-1,1));
        }
        return $d;
    }

    sub get_bw_contrast {
        my ($code) = @_;
        my ($r, $g, $b, $lum, $fg);

        $code =~ s/^#//;
        $r = &hex2decimal(substr($code,0,2));
        $g = &hex2decimal(substr($code,2,2));
        $b = &hex2decimal(substr($code,4,2));
        $lum = 0.2126 * ($r/255)**2.2
             + 0.7152 * ($g/255)**2.2
             + 0.0722 * ($b/255)**2.2;
        $fg = ($lum < 0.4) ? "white" : "black";
        return $fg;
    }
